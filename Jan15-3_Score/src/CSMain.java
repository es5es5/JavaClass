
public class CSMain {

	public static void main(String[] args) {
		int age = 26;

		switch (age) {
		case 10:
			System.out.println("어서와");
			break;
		case 20:
			System.out.println("어서오세요");
			break;
		case 30:
			System.out.println("어서오십시오");
			break;
		default:
			System.out.println("??");
			break;
		}
	}

}
